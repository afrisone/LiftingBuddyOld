package afrisone.liftingbuddy;


public final class LiftingDB {
    private int caloriesDB;
    private int proteinDB;
    private int fatDB;
    private int carbohydratesDB;

    LiftingDB() {}

    LiftingDB(int calories, int protein, int fat, int carbohydrates) {
        caloriesDB = calories;
        proteinDB = protein;
        fatDB = fat;
        carbohydratesDB = carbohydrates;
    }



}

